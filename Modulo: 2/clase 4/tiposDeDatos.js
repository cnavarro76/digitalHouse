// Dato del tipo number
let edad = 45;
console.log(edad);

// Dato del tipo number con decimales
let precio = 150.50
console.log(precio)

// Dato del tipo String con comillas dobles
let nombre = "Carlos";
console.log(nombre);

// Dato del tipo string con comillas simples
let apellido = 'Navarro';
console.log(apellido);

// Dato del tipo booleano con valor true
let verdadero = true;
console.log(verdadero);

// Dato del tipo booleano con valor false
let falso = false;
console.log(falso);

// Dato del tipo NaN
let notANumber = NaN;
console.log(notANumber);

// Dato del tipo null
let nulo = null;
console.log(nulo);

// Dato undefined
let sinValor = "Sin Valor";
console.log(sinValor);

// Dato del tipo objeto literal
let persona = {
    nombre: 'Carlos',
    edad: 45
}
console.log(persona);

// Dato del tipo array
let paises = ["Argentina", "Colombia", "Mexico", "Venezuela" ];
console.log(paises);

